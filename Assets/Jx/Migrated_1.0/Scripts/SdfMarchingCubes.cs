// SdfMarchingCubes.cs
//
// Marching Cubes extraction from an SdfColliderAsset grid.
// Produces a Unity Mesh representing a single iso-level of the SDF.
//
// ── Usage ─────────────────────────────────────────────────────────────────────
// Call Extract(asset, isoLevel) to get a Mesh in LOCAL space of the SDF grid.
// The returned mesh must be destroyed by the caller when no longer needed.
//
// Typical calls from the editor visualiser:
//   surfaceMesh  = SdfMarchingCubes.Extract(asset,  0.00f);  // green: contact surface
//   interiorMesh = SdfMarchingCubes.Extract(asset, -0.05f);  // red:   solid interior
//
// ── Algorithm ─────────────────────────────────────────────────────────────────
// Standard Lorensen & Cline 1987 Marching Cubes.
// Each voxel cube is classified by the 256-case lookup table.
// Vertices are linearly interpolated on each edge.
// Normals are computed from the SDF gradient (central differences) at each vertex.
//
// ── Coordinate space ──────────────────────────────────────────────────────────
// Output mesh is in the SDF grid's LOCAL space (same as asset.BoundsMin/Max).
// The XpbdSdfCollider component's transform converts this to world space when
// rendering the gizmo.

