		/// <summary>
		/// Extract an iso-surface mesh from the SDF grid at <paramref name="isoLevel"/>.
		/// Returns null if the asset is not baked or the iso-level produces no surface.
		/// The returned Mesh must be destroyed by the caller.
		/// </summary>
		public static Mesh Extract(SdfColliderAsset asset, float isoLevel = 0f)
		{
			if (asset == null || !asset.IsBaked)
				return null;

			int rx = asset.ResX, ry = asset.ResY, rz = asset.ResZ;
			Vector3 bMin  = asset.BoundsMin;
			Vector3 cellSz = asset.CellSize;

			var verts   = new List<Vector3>(4096);
			var norms   = new List<Vector3>(4096);
			var indices = new List<int>(8192);

			// Walk every cube (ix,iy,iz) → (ix+1,iy+1,iz+1)
			for (int iz = 0; iz < rz - 1; iz++)
			{
				for (int iy = 0; iy < ry - 1; iy++)
				{
					for (int ix = 0; ix < rx - 1; ix++)
					{
						// 8 corner values, world-local positions
						float[] val = new float[8];
						Vector3[] pos = new Vector3[8];
						for (int k = 0; k < 8; k++)
						{
							int cx = ix + CornerOffsets[k].x;
							int cy = iy + CornerOffsets[k].y;
							int cz = iz + CornerOffsets[k].z;
							val[k] = asset.SdfGrid[cx + cy * rx + cz * rx * ry];
							pos[k] = bMin + new Vector3(
								cx * cellSz.x,
								cy * cellSz.y,
								cz * cellSz.z);
						}

						MarchCube(val, pos, isoLevel, asset, verts, norms, indices);
					}
				}
			}

			if (verts.Count == 0)
				return null;

			var mesh = new Mesh { name = $"SDF_iso{isoLevel:F3}" };
			mesh.indexFormat = verts.Count > 65535
				? UnityEngine.Rendering.IndexFormat.UInt32
				: UnityEngine.Rendering.IndexFormat.UInt16;
			mesh.SetVertices(verts);
			mesh.SetNormals(norms);
			mesh.SetTriangles(indices, 0);
			mesh.RecalculateBounds();
			return mesh;
		}

		// ── Marching cubes core ───────────────────────────────────────────────

		static void MarchCube(
			float[] val, Vector3[] pos, float iso,
			SdfColliderAsset asset,
			List<Vector3> verts, List<Vector3> norms, List<int> indices)
		{
			// Build 8-bit cube index
			int cubeIdx = 0;
			for (int i = 0; i < 8; i++)
			{
				if (val[i] < iso)
					cubeIdx |= (1 << i);
			}

			if (cubeIdx == 0 || cubeIdx == 255)
				return;

			// Find active edges from edge table
			int edgeMask = EdgeTable[cubeIdx];
			if (edgeMask == 0)
				return;

			// Interpolate vertex on each active edge
			var edgeVerts = new Vector3[12];
			var edgeNorms = new Vector3[12];
			for (int e = 0; e < 12; e++)
			{
				if ((edgeMask & (1 << e)) == 0)
					continue;
				int a = EdgeConnections[e, 0];
				int b = EdgeConnections[e, 1];
				float t = Mathf.Clamp01((iso - val[a]) / (val[b] - val[a] + 1e-10f));
				Vector3 p = Vector3.Lerp(pos[a], pos[b], t);
				edgeVerts[e] = p;
				edgeNorms[e] = GradientAt(asset, p);
			}

			// Emit triangles from tri table
			int baseIdx = cubeIdx * 16;
			for (int i = 0; TriTable[baseIdx + i] != -1; i += 3)
			{
				int e0 = TriTable[baseIdx + i];
				int e1 = TriTable[baseIdx + i + 1];
				int e2 = TriTable[baseIdx + i + 2];

				int vi = verts.Count;
				verts.Add(edgeVerts[e0]);
				verts.Add(edgeVerts[e1]);
				verts.Add(edgeVerts[e2]);
				norms.Add(edgeNorms[e0]);
				norms.Add(edgeNorms[e1]);
				norms.Add(edgeNorms[e2]);
				indices.Add(vi);
				indices.Add(vi + 1);
				indices.Add(vi + 2);
			}
		}

		// Central-difference gradient of the SDF grid at local-space point p.
		static Vector3 GradientAt(SdfColliderAsset asset, Vector3 p)
		{
			float h = Mathf.Max(asset.CellSize.x, Mathf.Max(asset.CellSize.y, asset.CellSize.z));
			float dx = asset.SampleLocal(p + new Vector3(h, 0, 0))
					 - asset.SampleLocal(p - new Vector3(h, 0, 0));
			float dy = asset.SampleLocal(p + new Vector3(0, h, 0))
					 - asset.SampleLocal(p - new Vector3(0, h, 0));
			float dz = asset.SampleLocal(p + new Vector3(0, 0, h))
					 - asset.SampleLocal(p - new Vector3(0, 0, h));
			var g = new Vector3(dx, dy, dz);
			return g.sqrMagnitude > 1e-10f ? g.normalized : Vector3.up;
		}

